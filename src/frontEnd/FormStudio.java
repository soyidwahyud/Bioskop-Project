    private void tblStudioMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_tblStudioMouseClicked
        // TODO add your handling code here:
        DefaultTableModel model = (DefaultTableModel)tblStudio.getModel();
        int row = tblStudio.getSelectedRow();
        Studio s = new Studio().getByidStudio(Integer.parseInt(model.getValueAt(row, 0).toString()));
        
        txtIdStudio.setText(String.valueOf(s.getIdStudio()));
        cmbJudulFilm.getModel().setSelectedItem(s.getFilm());
        txtStudio.setText(s.getStudio());
    }//GEN-LAST:event_tblStudioMouseClicked

