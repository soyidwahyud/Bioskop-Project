    private void btnSaveActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnSaveActionPerformed
        // TODO add your handling code here:
        Studio s = new Studio();
        s.setIdStudio(Integer.parseInt(txtIdStudio.getText()));
        s.setFilm((Film)cmbJudulFilm.getSelectedItem());
        s.setStudio(txtStudio.getText());
        s.save();
        txtIdStudio.setText(Integer.toString(s.getIdStudio()));
        showData();
        Pembelian p = new Pembelian();
        p.setIdPembelian(0);
        p.setStudio(s);
        p.getKursi().setKode("A0");
        p.getKursi().setStatus("book");
        p.save();
        
    }//GEN-LAST:event_btnSaveActionPerformed

    private void btnDeleteActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnDeleteActionPerformed
        // TODO add your handling code here:
        DefaultTableModel model = (DefaultTableModel)tblStudio.getModel();
        int row = tblStudio.getSelectedRow();
        Studio s = new Studio().getByidStudio(Integer.parseInt(model.getValueAt(row, 0).toString()));
        s.delete();
        emptyForm();
        showData();
    }//GEN-LAST:event_btnDeleteActionPerformed

