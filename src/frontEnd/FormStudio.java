    public void showData(){
        String[] kolom = {"ID","Studio","Film"};
        ArrayList<Studio> list = new Studio().getAll();
        Object row[] = new Object[3];
        tblStudio.setModel(new DefaultTableModel(new Object[][]{}, kolom));
        
        for (int i = 0; i < list.size(); i++) {
            row[0]= list.get(i).getIdStudio();
            row[1]= list.get(i).getStudio();
            row[2]= list.get(i).getFilm().getJudul();
            ((DefaultTableModel)tblStudio.getModel()).addRow(row);
        }        
    }
    public void setCmbFilm(){
        cmbJudulFilm.setModel(new DefaultComboBoxModel(new Film().getAll().toArray()));
    }
