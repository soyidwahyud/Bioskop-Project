    private void jDeleteActionPerformed(java.awt.event.ActionEvent evt) {                                        
        // TODO add your handling code here:
        DefaultTableModel model = (DefaultTableModel)TableMinuman.getModel();
        int row = TableMinuman.getSelectedRow();
        Minuman m = new Minuman().getById(Integer.parseInt(model.getValueAt(row, 0).toString()));
        m.delete();
        kosongkanForm();
        tampilData();
    }                                       

    private void jSaveActionPerformed(java.awt.event.ActionEvent evt) {                                      
        // TODO add your handling code here:
        Minuman m = new Minuman();
        m.setIdminuman(Integer.parseInt(jTextMinuman.getText()));
        m.setNama_minuman(jTextnamaMinum.getText());
        m.setHarga(Integer.parseInt(jTextHarga.getText().toString()));
        m.save();
        jTextMinuman.setText(Integer.toString(m.getIdminuman()));
        tampilData();
    }                                     

