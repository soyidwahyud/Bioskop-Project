    public void tampilData(){
        String[] kolom = {"idminuman","nama_minuman","harga"};
        ArrayList<Minuman> list = new Minuman().getAll();
        Object rowData[] = new Object[3];
        TableMinuman.setModel(new DefaultTableModel(new Object[][]{}, kolom));
        
        for (Minuman minuman : list) {
            rowData[0] = minuman.getIdminuman();
            rowData[1] = minuman.getNama_minuman();
            rowData[2] = minuman.getHarga();
            ((DefaultTableModel)TableMinuman.getModel()).addRow(rowData);
        }
    }
    public void cari(String keyword){
        String[] kolom = {"idminuman", "nama_minuman","harga"};
        ArrayList<Makanan> list = new Makanan().search(keyword);
        Object rowData[] = new Object[3];
        TableMinuman.setModel(new DefaultTableModel(new Object[][]{}, kolom));
        for (Makanan makanan : list) {
            rowData[0] = makanan.getIdmakanan();
            rowData[1] = makanan.getNama_makanan();
            rowData[2] = makanan.getHarga();
            ((DefaultTableModel)TableMinuman.getModel()).addRow(rowData);
        }
    }
