    private void tblTransaksiMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_tblTransaksiMouseClicked
        // TODO add your handling code here:
        DefaultTableModel model = (DefaultTableModel)tblTransaksi.getModel();
        int row = tblTransaksi.getSelectedRow();
        Studio s = new Studio().getByidStudio(Integer.parseInt(model.getValueAt(row, 0).toString()));

        txtIdStudio.setText(String.valueOf(s.getIdStudio()));
        cmbJudulFilm.getModel().setSelectedItem(s.getFilm());
        txtStudio.setText(s.getStudio());
    }//GEN-LAST:event_tblTransaksiMouseClicked

