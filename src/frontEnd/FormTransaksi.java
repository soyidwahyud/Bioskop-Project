    private void btnBayarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnBayarActionPerformed
        // TODO add your handling code here:
        Studio s = new Studio();
        s.setIdStudio(Integer.parseInt(txtIdStudio.getText()));
        s.setFilm((Film)cmbJudulFilm.getSelectedItem());
        s.setStudio(txtStudio.getText());
        s.save();
        txtIdStudio.setText(Integer.toString(s.getIdStudio()));
        showData();
    }//GEN-LAST:event_btnBayarActionPerformed

    private void btnBaruActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnBaruActionPerformed
        // TODO add your handling code here:
        emptyForm();
        DefaultTableModel model = (DefaultTableModel)tblTransaksi.getModel();
        int row = tblTransaksi.getSelectedRow();
        Transaksi transaksi = new Transaksi().getByIdTransaksi(Integer.parseInt(model.getValueAt(row, 0).toString()));
        transaksi.delete();
        emptyForm();
        showData();
    }//GEN-LAST:event_btnBaruActionPerformed

