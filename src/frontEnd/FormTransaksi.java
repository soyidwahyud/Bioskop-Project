    public void showData()
    {
        String[] kolom = {"ID","Studio","Film","kursi","Total harga"};
        ArrayList<Transaksi> list = new Transaksi().getAll();
        Object row[] = new Object[5];
        tblTransaksi.setModel(new DefaultTableModel(new Object[][]{}, kolom));
        
        for (int i = 0; i < list.size(); i++) {
            row[0]= list.get(i).getIdTransaksi();
            row[1]= list.get(i).getStudio();
            row[2]= list.get(i).getJudul();
            row[3]= list.get(i).getKodekursi();
            row[4]= list.get(i).getTotal();
            ((DefaultTableModel)tblTransaksi.getModel()).addRow(row);
        }        
    }
